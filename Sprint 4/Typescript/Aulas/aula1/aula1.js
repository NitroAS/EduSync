// Orientação á Objetos 


// Onde surgiu esse termo?

//  Termo surgiu com : Alan Kay
//  Primeira Linguagem a usar : Simula67
//  1960 á 1990


// Conceito Geral:

// Real -> Virtual


// Vantagens da POO:

//  Todo software orientado à objeto é confiável (ao alterar uma parte nenhuma outra é afetada)

// O software orientado a objeto é oportuno (ao dividir tudo em partes, várias delas podem ser desenvolvidas em parelelo) 

// O software orientado a objeto é manutenível (atualizar um software é mais fácil, uma pequena modificação beneficia todas as partes que usarem o objeto)


// Outras Vantagens da POO:

// Ele também é extensível (o software não é estático, deve crescer para permanecer útil)

// O software orientado a objeto é reutilizável (podemos usar o objeto de um sistema que criamos em outro sistema que viermos a criar)

// Ele é natural (todo software orientado a objeto tem que ser natural, pois é mais fácil de entender, se preocupa mais com a funcionalidade do que com os detalhes de implementação)


// Desvantagens da POO:

// A execução de uma aplicação orientada a objetos é MAIS LENT do que o que vemos na programação estruturada, por exemplo 

// Tempo de Execução:

//  Isso acontece devido à complecidade do modelo, que traz representações na forma de classes. Porém, no momento em que estamos, tecnoligicamente essa execução mais lenta não nem percebida pelo desenvolvedor e nem pelo usuário do software.
